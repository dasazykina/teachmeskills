package lesson2;

public class Number6B {
    public static void main(String[] args) {
        int a = 4;
        int b = 5;
        //  a + b = 9;
        a = a + b;
        // a = 9;
        b = a - b;
        // b = 4;
        a = a - b;
        // a = 5
        System.out.println(a);
        System.out.println(b);
    }
}
